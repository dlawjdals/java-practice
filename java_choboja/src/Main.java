
public class Main {
    public static void main(String[] args) {
        // 주어진 값들
        double doubleValue = 2.71828;

        // double에서 다른 타임으로의 형변환 결과 출력
        System.out.println("\n=== double 형변환 ===");
        System.out.println("1. double -> byte: " + (byte)doubleValue);
        System.out.println("1. double -> short: " + (short)doubleValue);
        System.out.println("1. double -> int: " + (int)doubleValue);
        System.out.println("1. double -> long: " + (long)doubleValue);
        System.out.println("1. double -> float: " + (float)doubleValue);

    }
}